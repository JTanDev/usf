// What is the difference between var and let?
//var variables can be re-declared multiple times, where let can only be declared once. also let is block scoped.

// What is the difference between var and const?
//var can be changed and const can not.

// What is the difference between let and const?
//let allows you to change the value of a variable, while const does not. elements within an object or array declared with const can be changed or added to though.

// What is hoisting?
//when using var, the variable name is loaded before the rest of the js file. so if you call that variable before it is assigned a value, you get back undefined, rather than an error like you would with let or const.
